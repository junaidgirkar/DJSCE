import requests
from bs4 import BeautifulSoup

URL = "https://en.wikipedia.org/wiki/Dwarkadas_J._Sanghvi_College_of_Engineering"
page = requests.get(URL)

soup = BeautifulSoup(page.content, "html.parser")

# Find element by ID
results = soup.find(id="content")
print(results.prettify())

# Find elements by tag and class name
page_titles = results.find_all("span", class_="mw-headline")
print("Page Titles:\n", page_titles)

for title in page_titles:
    print(title, end="\n"*2)

# Extract text from HTML elements
for title in page_titles:
    print(title.text.strip())

# Extracting Main class containing departments
results2 = soup.find(class_="mw-parser-output")
results2.prettify()

departments = results2.find_all("a", class_="mw-redirect")
departments

for department in departments:
  print(department.text)